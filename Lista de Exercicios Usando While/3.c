// Exercício 3: Imprimir os números de 10 a 1 (ordem decrescente)
#include <stdio.h>

int main() {
    int i = 10;

    // Enquanto i for maior ou igual a 1
    while (i >= 1) {
        printf("%d\n", i); // Imprime o valor de i
        i--;               // Decrementa i em 1
    }

    return 0;
}
